void	ft_putchar(char c);

int		main(int ac, char **av)
{
	int i;

	i = 1;
	while (i < ac)
	{
		while (*av[i] != '\0')
			ft_putchar(*av[i]++);
		ft_putchar('\n');
		i++;
	}
	return (0);
}
