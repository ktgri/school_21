int		ft_iterative_factorial(int nb)
{
	int a;

	a = nb;
	if (nb == 0 || nb == 1)
		return (1);
	if (nb < 0 || nb > 12)
		return (0);
	if (nb > 0)
	{
		while (nb > 1)
		{
			a = a * (nb - 1);
			nb--;
		}
	}
	else
		return (0);
	return (a);
}
