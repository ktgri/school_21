#include "includes/libft.h"

void	ft_striter(char *s, void (*f)(char*))
{
	size_t	len;

	if (s != NULL && f != NULL)
	{
		len = ft_strlen(s);
		while (len > 0)
		{
			f(s);
			s++;
			len--;
		}
	}
}
