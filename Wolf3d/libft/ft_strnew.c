#include "includes/libft.h"

char	*ft_strnew(size_t size)
{
	void	*ptr1;

	if (size > size + 1)
		return (NULL);
	ptr1 = ft_memalloc(size + 1);
	if (ptr1 == NULL)
	{
		return (NULL);
	}
	return ((char*)ptr1);
}
