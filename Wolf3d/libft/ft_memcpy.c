#include "includes/libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char	*dst1;
	unsigned char	*src1;

	dst1 = (unsigned char*)dst;
	src1 = (unsigned char*)src;
	while (n-- > 0)
	{
		*dst1 = *src1;
		dst1++;
		src1++;
	}
	return (dst);
}
